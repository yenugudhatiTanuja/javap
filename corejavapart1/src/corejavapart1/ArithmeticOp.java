package corejavapart1;

public class ArithmeticOp {
	public static void main(String[] args) {
		int sum,mul,div,sub,mod;
		int a=10,b=15;
		sum=a+b;
		mul=a*b;
		div=a/b;
		sub=a-b;
		mod=a%b;
		System.out.println(" sum is "+sum+ " mul is  "+mul+ "  div "+div+" sub "+sub+"  mod  "+mod);
	}

}
